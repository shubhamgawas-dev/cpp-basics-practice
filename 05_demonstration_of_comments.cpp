#include <iostream>
using namespace std;

int main()
{
    // This is a single line comment.
    cout << "Demonstration of Comments in c++" << endl;

    /*
    This is a Multi line comment
    It can be used for multiple lines.
    Useful for explaning larger sections of code.
    */

    int a = 21; // Variable declaration with single line comment.
    cout << "Value of a : " << a << endl;

    return 0;
}