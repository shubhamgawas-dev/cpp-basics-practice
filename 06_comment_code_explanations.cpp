#include <iostream>  //Includes input-output stream library
using namespace std; // Allows use of standard names like cout without std::

int main() // Main function where program execution is start.
{
    int num1 = 12; // Declare integer vaiable num1 and assign the value 12
    int num2 = 20; // Declare integer vaiable num2 and assign the value 20

    int sum = num1 + num2; // Added num1 and num2 and stored in sum.

    cout << "Sum is : " << sum << endl; // print the sum.

    return 0; // End of program(returns 0 to operating system).
}